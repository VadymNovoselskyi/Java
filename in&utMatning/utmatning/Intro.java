package utmatning;

public class Intro {

	public static void main(String[] args) {
		//Utmatning
		System.out.print("Hello world!");
		System.out.print("Hello world again!");
		
		//Utskrift med radbyte
		System.out.println("New row?");		//radbyte efter
		System.out.println("Now then?");
		
		// ´\´ skapar olika formatteringsalternativ för text 
		System.out.println("The begging of row one \nThere is the second row");		// ´\n´ ger mig en ny rad
		System.out.println("To tab \tso you get space");		// ´\t´ ger mig mer luft i raden
		
		//extra radbyte
		System.out.println();
		System.out.println("WOW");
		
		//För snyggare utskriver kolla upp ´System.out.printf()´
		
	}

}
